export default function calculatePlinko(server, client, nonce, risk = "high", row = 8) {
    const hmacSha512Result = getGameHash512(server, client, nonce);
    const rollArray = [...new Array(row).keys()].map(i => Math.floor(byte2Num(hash2Bytes(hmacSha512Result, i)) * 2));
    const index = rollArray.reduce((acc, i) => (i + acc), 0);
    const payoutArray = plinkoPayouts[risk][row];
    const resultList = { payoutArray, rollArray, index }

    return { resultList, hmacSha512Result }
}